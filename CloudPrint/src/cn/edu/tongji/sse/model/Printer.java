package cn.edu.tongji.sse.model;



public class Printer {
	
	private String id;
	private String name;
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String printerId) {
		this.id = printerId;
	}
	public String getName() {
		return name;
	}
	public void setName(String printerName) {
		this.name = printerName;
	}
	
	
	
}
